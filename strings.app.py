course = "Python Programming"
# length of sting which means length of charagters always use parenteses
print(len(course))
print(course[0])  # for specific characters use this method in a string
print(course[-1])
print(course[0:3])
# starts at the begining of the line and goes to the end [0:] (dont includeend index)
print(course[0:])
print(course[:3])  # including the end index
print(course[:])  # adding nothing gives the original line
